#include "3-calc.h"

/**
 * op_add - To return the sum of a and b
 * @a: first interger
 * @b: Second interger
 * Return: The sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - To return the difference of a and b
 * @a: first interger
 * @b: second interger
 * Return: The difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - To return the product of a and b
 * @a: first interger
 * @b: second interger
 * Return: The product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - To return the result of the division of a by b
 * @a: first interger
 * @b: second interger
 * Return: The quotient of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - To return the remainder of the division of a by b
 * @a: first interger
 * @b: second interger
 * Return: The modulus of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
